
package First;

public class Parameter {
	
		// the variables a and b are parameters
		public static int multiply(int a, int b)
		{
			return a + b;
		}

		public static void main(String[] args)
		{
			int x = 2;
			int y = 5;

			int sum = multiply(x, y);

			System.out.println("SUM IS: " + sum);
		}
	}
	
	


